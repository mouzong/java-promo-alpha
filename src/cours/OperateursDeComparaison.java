package cours;

public class OperateursDeComparaison {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println("\t\t a = " + a + "\t b = " + b);
        System.out.println("\t\t ------------------------");
        System.out.println("\t\t a == b = " + (a == b) );
        System.out.println("\t\t a != b = " + (a != b) );
        System.out.println("\t\t a > b = " + (a > b) );
        System.out.println("\t\t a < b = " + (a < b) );
        System.out.println("\t\t b >= a = " + (b >= a) );
        System.out.println("\t\t b <= a = " + (b <= a) );
    }
}
