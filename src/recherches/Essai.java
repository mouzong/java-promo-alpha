package recherches;

public class Essai {
    public int addition(int a, int b) {
        int somme = 0;
        somme = a + b;
        return somme;
    }

    public int soustraire(int a, int b) {
        int soustraction = a - b;
        return soustraction;
    }

    public int multiplier(int a, int b) {
        int multiplication = a * b;
        return multiplication;
    }

    public double diviser(double a, double b) {
        double division = a / b;
        return division;
    }
}
